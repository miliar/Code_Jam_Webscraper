#!/usr/bin/env python
import sys

code = {' ': ' ', 'a': 'y', 'c': 'e', 'b': 'h', 'e': 'o', 'd': 's', 'g': 'v', 'f': 'c', 'i': 'd', 'h': 'x', 'k': 'i', 'j': 'u', 'm': 'l', 'l': 'g', 'o': 'k', 'n': 'b', 'q': 'z', 'p': 'r', 's': 'n', 'r': 't', 'u': 'j', 't': 'w', 'w': 'f', 'v': 'p', 'y': 'a', 'x': 'm', 'z': 'q'}

def translate(s):
    for i in s:
        yield code[i]

for case in range(1, int(sys.stdin.readline()) + 1):
    print("Case #%d: %s" % (case, "".join(list(translate(sys.stdin.readline()[:-1])))))
