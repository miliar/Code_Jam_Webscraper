class Case:
    def __str__(self):
        output=[]
        for i in sorted(set(dir(self))-(set(dir(Case())))):
            output.append(str(i)+" : "+str(getattr(self, i)))
        return '\n'.join(output)

    def solve(self):
        print(str(self))

        result=0
        for i in range(0,self.a):
            for j in range (0,self.b):
                if i&j<self.k:
                    result+=1

        return result

