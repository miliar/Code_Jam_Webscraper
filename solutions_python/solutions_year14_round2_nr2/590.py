import os, subprocess
import math

class Input:
    @staticmethod
    def parse():
        if False:
            problem='problem.txt'
        else:
            path=os.path.join(os.environ['HOME'],'Downloads')
            problem=subprocess.check_output(['ls','-t',path]).split('\n')[0]
            problem=os.path.join(path,problem)
        print(problem)
        Input.file=open(problem,'r')
        return int(Input.file.readline())

    @staticmethod
    def case():
        for i in range(0, Input.parse()):
            yield Input.parseCase()

    @staticmethod
    def parseCase():
        case = Case()
        values=Input.file.readline().strip().split(' ')
        case.a, case.b, case.k = [ int(i) for i in values ]

        return case


