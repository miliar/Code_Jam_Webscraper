    "class Problem(CodejamProblem):\n",
    "    def __init__(self, input):\n",
    "        CodejamProblem.__init__(self, input)\n",
    "        \n",
    "        T = self.readint()\n",
    "        for i in range(T):\n",
    "            self.cases.append(self.readints())\n",
    "            \n",
    "    def solvecase(self, case):\n",
    "        length, count = case\n",
    "        R.<x> = PolynomialRing(ZZ)\n",
    "        coins = []\n",
    "        i = 0\n",
    "        while len(coins) < count:\n",
    "            coin = ZZ(i.str(base=2), base=4)\n",
    "            coin = (4*coin + 1 + 2^(length-2)) * 3\n",
    "            coin = coin.str(base=2)\n",
    "            assert len(coin) == length\n",
    "            coins.append(coin)\n",
    "            i += 1\n",
    "        return coins\n",
    "    \n",
    "    @staticmethod\n",
    "    def formatanswer(coins):\n",
    "        out = \"\"\n",
    "        for coin in coins:\n",
    "            lst = [coin]\n",
    "            for b in range(2,11):\n",
    "                n = ZZ(coin, base=b)\n",
    "                d = b+1\n",
    "                assert n % d == 0\n",
    "                assert 1 < d < n\n",
    "                lst.append(d)\n",
    "            out += \" \".join(str(x) for x in lst) + \"\\n\"\n",
    "        return out"
