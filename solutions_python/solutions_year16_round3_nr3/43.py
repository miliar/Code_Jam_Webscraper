    "def do_solve(J, P, S, K):\n",
    "    # trivial upper bound for outfits\n",
    "    bound1 = J*P*S\n",
    "    # upper bound based on two-garment combinations\n",
    "    bound2 = min([a*b*min(K, c) for (a,b,c) in it.permutations([J, P, S])])\n",
    "    # \n",
    "    bound = min(bound1, bound2)\n",
    "    \n",
    "    res = str(bound)\n",
    "    \n",
    "    listJ = list(range(1, J+1))\n",
    "    listP = list(range(1, P+1))\n",
    "    listS = list(range(1, S+1))\n",
    "    \n",
    "    outfit_lines = []\n",
    "    while len(outfit_lines) != bound:\n",
    "        # count how often two-garment combinations were worn\n",
    "        wornJP = Counter()\n",
    "        wornJS = Counter()\n",
    "        wornPS = Counter()\n",
    "        outfit_lines = []\n",
    "        i = 0\n",
    "        products = list(it.product(listJ, listP, listS))\n",
    "        shuffle(products) # oh, well...\n",
    "        for prod in products:\n",
    "            j, p, s = prod\n",
    "            # check if two-garment combination is already \"used up\"\n",
    "            # no need to check for duplicate outfit since using it.product\n",
    "            if wornJP[j, p] == K or wornJS[j, s] == K or wornPS[p, s] == K:\n",
    "                continue\n",
    "\n",
    "            outfit_lines.append(' '.join(map(str, prod)))\n",
    "\n",
    "            wornJP[j, p] += 1\n",
    "            wornJS[j, s] += 1\n",
    "            wornPS[p, s] += 1\n",
    "\n",
    "            i += 1\n",
    "            if i == bound:\n",
    "                # done\n",
    "                break\n",
    "            \n",
    "    if len(outfit_lines) != bound:\n",
    "        print \"ERR\", len(outfit_lines), bound\n",
    "    \n",
    "    for line in outfit_lines:\n",
    "        res += '\\n' + line\n",
    "    \n",
    "    return res"
