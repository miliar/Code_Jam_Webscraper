    "def do_solve(B, M):\n",
    "    # full graph -> max possible paths\n",
    "    if M > 2**(B-2):\n",
    "        return \"IMPOSSIBLE\"\n",
    "    res = \"POSSIBLE\"\n",
    "    \n",
    "    e = np.zeros((B,B), dtype=bool)\n",
    "    # start with one complete graph without loops\n",
    "    # first node -> to all other nodes\n",
    "    # second node -> to all other except first node\n",
    "    # ...\n",
    "    # last node -> to no node\n",
    "    for i in xrange(B):\n",
    "        for j in xrange(i+1, B):\n",
    "            e[i][j] = True\n",
    "    \n",
    "    # number of overhead paths \n",
    "    overhead = 2**(B-2) - M\n",
    "    # first node edges: 0DCBA1\n",
    "    # A -> 1 path, B -> 2 paths, C -> 4 paths, etc.\n",
    "    # M >= 1 -> always keep last edge (direct slide from 1 to B)\n",
    "    for j in xrange(1, B-1):\n",
    "        # paths available via current edge from node 1\n",
    "        n_paths = 2**(B-j-2)\n",
    "        if overhead >= n_paths:\n",
    "            e[0][j] = False\n",
    "            overhead -= n_paths\n",
    "    # output\n",
    "    for edge in e:\n",
    "        res += '\\n'\n",
    "        res += ''.join(map(str, map(int, edge)))\n",
    "    return res"
