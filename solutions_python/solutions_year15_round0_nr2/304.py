def div(m,d):
	if d == 1:
		return (m,)
		
	if (m,d) in div.memo:
		return div.memo[(m,d)]
		
	x = (m+d-1)//d
	res = (x,) + div(m-x, d-1)
	div.memo[(m,d)] = res
	return res
