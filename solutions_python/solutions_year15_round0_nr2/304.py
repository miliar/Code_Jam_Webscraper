def bsearch(key, l):
	if l == []:
		return 0
	if len(l) == 1:
		if key >= l[0]:
			return 0
		else:
			return 1
	
	m = len(l)//2
	
	
	if key == l[m]: return m
	if key < l[m]:
		return m + bsearch(key, l[m:])
	if key > l[m]:
		return bsearch(key, l[:m])
		
	raise
		
