def findFalse(p):
	for i in range(len(p)-1,-1,-1):
		if not p[i]:
			return i
	return -1

def solve(S):
	pancakes = [ False if c == '-' else True for c in S ]
	count = 0

	while False in pancakes:
		count+=1
		k = findFalse(pancakes)
		pancakes = [ False if v == True else True for v in pancakes[:k+1] ] + pancakes[k+1:]

	return count

